#What is Data Types 
#Data Types define the type of data stored in a variable.

#String(str)
name = "Nasir"
print(name)
print(type(name))
#output
#<class 'str'>

#Integer(int)
age = 25
print(age)
print(type(age))

#Float(float)
height = 5.9
print(height)
print(type(height))

#Boolean(bool)
is_pass = True
print(is_pass)
print(type(is_pass))

#List
fruits = ["apple", "banana","mango"]
print(fruits)
print(type(fruits))

marks = [77,86,81,92]
print(marks)
print(type(marks))

#Tuple
subjects = ("Computer","Mathematics","Physics")
print(subjects)
print(type(subjects))

#Dictionary
student = {
    "name" : "Nasir",
    "age" : 25,
    "height" :5.9
}
print(student)
print(type(student))

#Set
numbers = {1,2,3,5,6}
print(numbers)
print(type(numbers))

#None Type 
data = None
print(data)
print(type(data))


