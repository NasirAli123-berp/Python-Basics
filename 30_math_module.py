#What is the Math Module?
#The math module is a built-in Python module that provides mathematical functions and constants.

#Why do we use the math module?
#we use the math module to perform advance mathematical calculations.

import math 
#1. sqrt()

print(math.sqrt(64)) # square root 
#2. pow()
print(math.pow(2,5))  #2⁵
#3. ceil()
print(math.ceil(4.3))  # upper integer 
#4. floor()
print(math.floor(7.9))  # lower integer 
#5. factorial()
print(math.factorial(4)) # 5*4*3*2*1
#6. pi
print(math.pi)
#7. e
print(math.e)
#8. fabs()
print(math.fabs(-25)) # absolute value 
#9. trunc()
print(math.trunc(8.875))  # remove decimal 
#10. gcd()
print(math.gcd(20,30,25))  # greatest common divisor

